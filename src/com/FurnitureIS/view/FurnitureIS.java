    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblFurnitureInfo.getSelectedRow(); 

    if (selectedRow >= 0) {
        // Fetch new details from input fields
        String updatedID = tfFurnitureId.getText();
        String updatedName = tfname.getText();
        String updatedType = tfType.getText();
        String updatedBrand = tfBrand.getText();
        String updatedMaterial = (String) cbMaterial.getSelectedItem();

        // Validation for updated details
        if (ValidationUtil.isNullOrEmpty(updatedID) ||
            ValidationUtil.isNullOrEmpty(updatedName) ||
            ValidationUtil.isNullOrEmpty(updatedType) ||
            ValidationUtil.isNullOrEmpty(updatedBrand) ||
            ValidationUtil.isNullOrEmpty(updatedMaterial)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Please fill out all fields.", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!ValidationUtil.isValidFurnitureId(updatedID)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Furniture ID must start with letters and end with 7 digits.", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!ValidationUtil.isValidName(updatedName)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Name must only contain alphabets.", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!ValidationUtil.isValidType(updatedType)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Type must only contain alphabets.", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!ValidationUtil.isValidBrand(updatedBrand)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Brand must only contain alphabets.", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Update the furniture item in the list
        Furnituremodel furniture = furnitureList.get(selectedRow);
        furniture.setFurnitureID(updatedID);
        furniture.setName(updatedName);
        furniture.setType(updatedType);
        furniture.setBrand(updatedBrand);
        furniture.setMaterial(updatedMaterial);

        // Update the table row
        DefaultTableModel model = (DefaultTableModel) tblFurnitureInfo.getModel();
        model.setValueAt(updatedID, selectedRow, 0);
        model.setValueAt(updatedName, selectedRow, 1);
        model.setValueAt(updatedType, selectedRow, 2);
        model.setValueAt(updatedBrand, selectedRow, 3);
        model.setValueAt(updatedMaterial, selectedRow, 4);

        // Show success message
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Furniture item updated successfully!", 
            "Success", 
            JOptionPane.INFORMATION_MESSAGE);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Please select a row to update.", 
            "Update Error", 
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUpdateMouseClicked

