    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
                                        
    String ID = tfFurnitureId.getText();
    String Name = tfname.getText();
    String Type = tfType.getText();
    String Brand = tfBrand.getText();
    String Material = (String) cbMaterial.getSelectedItem();

    // Check for empty fields
    if (ValidationUtil.isNullOrEmpty(ID) ||
        ValidationUtil.isNullOrEmpty(Name) ||
        ValidationUtil.isNullOrEmpty(Type) ||
        ValidationUtil.isNullOrEmpty(Brand) ||
        ValidationUtil.isNullOrEmpty(Material)) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Please fill out all fields.", 
            "Validation Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Check for duplicate ID
    for (Furnituremodel furniture : furnitureList) {
        if (furniture.getFurnitureID().equals(ID)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Furniture ID already exists. Please use a unique ID.", 
                "Duplicate ID Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    // Validatin individual fields
    if (!ValidationUtil.isValidFurnitureId(ID)) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Furniture ID must start with letters and end with 7 digits.", 
            "Validation Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!ValidationUtil.isValidName(Name)) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Name must only contain alphabets.", 
            "Validation Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!ValidationUtil.isValidType(Type)) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Type must only contain alphabets.", 
            "Validation Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!ValidationUtil.isValidBrand(Brand)) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Brand must only contain alphabets.", 
            "Validation Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Default selected material if none is chosen
    if (ValidationUtil.isNullOrEmpty(Material)) {
        Material = "Engineered Wood";
    }

    // Create Furniture object
    Furnituremodel furniture = new Furnituremodel(ID, Name, Type, Brand, Material);
    registerFurniture(furniture);

    // Show success message
    javax.swing.JOptionPane.showMessageDialog(this, 
        "Furniture item added successfully!", 
        "Success", 
        JOptionPane.INFORMATION_MESSAGE);
                                    

    }//GEN-LAST:event_btnAddMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
       int selectedRow = tblFurnitureInfo.getSelectedRow(); 

    if (selectedRow >= 0) {
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this item?",
            "Confirm Deletion",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            furnitureList.remove(selectedRow);

            // Remove the row from the table
            DefaultTableModel model = (DefaultTableModel) tblFurnitureInfo.getModel();
            model.removeRow(selectedRow);

            lblLoginError.setText(""); 
            javax.swing.JOptionPane.showMessageDialog(this, "Item deleted successfully.");
        }
    } else {
        
        javax.swing.JOptionPane.showMessageDialog(this, 
                "\"Please select a row to delete.", 
                "Validation Error", 
                JOptionPane.ERROR_MESSAGE);
            
    } // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteMouseClicked

